# 这本书的名字叫‘5分钟精通Python’
print("这本书的名字叫'5分钟精通Python'")

# 这本书的名字叫"5分钟精通Python"
print('这本书的名字叫"5分钟精通Python"')

# 这本书的名字叫"5分钟'精通'Python"
# 转义字符(\n)
print("这本书的名字叫\"5分钟精通Python\"")
print('这本书的名字叫"5分钟\'精通\'Python"')

# 我    好    饿
# 转义字符\t相当于tab键
print("我\t好\t饿")

# D:\Documents\Developer\MyGithub
print("D:\\Documents\\Developer\\MyGithub")
print('D:\\Documents\\Developer\\MyGithub')

# r\R raw string：原始字符串，禁止转义
print(r'D:\Documents\Developer\MyGithub')
print(R'D:\Documents\Developer\MyGithub')

print('\\\\\\')
print(r'\\\\\\')

print('"5分钟\'精通\'Python"')
print(r'"5分钟\'精通\'Python"')